/**
 * formatters/markdown.js — Markdown report formatter for todo-harvest.
 *
 * Produces a well-structured Markdown report suitable for:
 * - GitHub PR comments
 * - Saved to TECHNICAL_DEBT.md in your repo
 * - Documentation wikis
 * - Pasting into Notion / Confluence
 */

import { relative } from 'node:path';

const SEVERITY_BADGE = {
  critical: '🔴 Critical',
  high:     '🟠 High',
  medium:   '🟡 Medium',
  low:      '🔵 Low'
};

/**
 * Format entries as a Markdown string.
 *
 * @param {import('../parser.js').TodoEntry[]} entries
 * @param {object} options
 * @param {string} options.rootDir - Project root for relative paths
 * @param {string} [options.projectName] - Project name for report title
 * @param {number|null} [options.maxTodos] - CI threshold
 * @returns {string}
 */
export function formatMarkdown(entries, options = {}) {
  const { rootDir, projectName = 'Project', maxTodos = null } = options;

  const lines = [];
  const now = new Date().toISOString().slice(0, 10);

  lines.push(`# TODO Harvest Report — ${projectName}`);
  lines.push(`> Generated by [todo-harvest](https://github.com/axiom-agent/todo-harvest) on ${now}`);
  lines.push('');

  if (entries.length === 0) {
    lines.push('✅ **No TODO-style comments found.** Clean codebase!');
    return lines.join('\n');
  }

  // Summary table
  const bySeverity = { critical: 0, high: 0, medium: 0, low: 0 };
  const byKeyword = {};
  for (const e of entries) {
    bySeverity[e.severity]++;
    byKeyword[e.keyword] = (byKeyword[e.keyword] || 0) + 1;
  }

  lines.push('## Summary');
  lines.push('');
  lines.push('| Total | 🔴 Critical | 🟠 High | 🟡 Medium | 🔵 Low |');
  lines.push('|-------|-------------|---------|-----------|--------|');
  lines.push(`| **${entries.length}** | ${bySeverity.critical} | ${bySeverity.high} | ${bySeverity.medium} | ${bySeverity.low} |`);
  lines.push('');

  // Keyword breakdown
  const kwEntries = Object.entries(byKeyword).sort((a, b) => b[1] - a[1]);
  if (kwEntries.length > 0) {
    lines.push('**By keyword:**');
    lines.push(kwEntries.map(([kw, count]) => `\`${kw}\`: ${count}`).join('  ·  '));
    lines.push('');
  }

  // CI threshold
  if (maxTodos !== null) {
    const passed = entries.length <= maxTodos;
    lines.push(passed
      ? `✅ **CI Threshold:** ${entries.length}/${maxTodos} (passing)`
      : `❌ **CI Threshold Exceeded:** ${entries.length}/${maxTodos} — resolve ${entries.length - maxTodos} item(s)`
    );
    lines.push('');
  }

  // Entries grouped by severity
  const severityOrder = ['critical', 'high', 'medium', 'low'];

  for (const severity of severityOrder) {
    const group = entries.filter(e => e.severity === severity);
    if (!group.length) continue;

    lines.push(`## ${SEVERITY_BADGE[severity]} (${group.length})`);
    lines.push('');

    // Group by file within severity
    const byFile = new Map();
    for (const entry of group) {
      const relPath = rootDir ? relative(rootDir, entry.file) : entry.file;
      if (!byFile.has(relPath)) byFile.set(relPath, []);
      byFile.get(relPath).push(entry);
    }

    for (const [filePath, fileEntries] of byFile) {
      lines.push(`### \`${filePath}\``);
      lines.push('');

      for (const entry of fileEntries) {
        const gitInfo = entry.author
          ? ` *(${entry.author}${entry.date ? `, ${entry.date}` : ''}${entry.commitHash ? `, \`${entry.commitHash}\`` : ''})*`
          : '';

        lines.push(`- **Line ${entry.line}** \`[${entry.keyword}]\` ${entry.message}${gitInfo}`);
      }
      lines.push('');
    }
  }

  lines.push('---');
  lines.push('*Generated by [todo-harvest](https://github.com/axiom-agent/todo-harvest)*');

  return lines.join('\n');
}
